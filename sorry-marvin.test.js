function collectChanges(result) {
  let last = 0, zero = true
  return  r => {
    if (r[0] < last && zero) {  // value change
      result.push(last)
      zero = false
    }
    last = r[0]
    if (last === 0) zero = true
  }
}

function collectMaxima(result) {
  let max = -1
  return  r => {
    if (r[0] > max) max = r[0]
    if (r[0] === 0 && (!result.length || result[result.length - 1] + 1 < max)) {
      result.push(max - 1)
    }
  }
}

function cleanup(p) {
  return p.replaceAll(/[^!>]/g, '')
}